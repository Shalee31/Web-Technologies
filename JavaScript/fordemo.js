document.write("for loop...<br/>");
var i;
for(i=0;i<10;i++){
    document.write('i:'+i+"<br/>");
}
i=0;
document.write("while loop...<br/>");
while(i<10){
    document.write('i:'+i+"<br/>");
    i++;
}
document.write("do while loop...<br/>");
i=0;
do{
    document.write('i:'+i+"<br/>");
    i++;
}while(i<10);